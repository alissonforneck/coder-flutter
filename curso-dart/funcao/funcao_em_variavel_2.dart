void main(List<String> args) {
  int adicao(int a, int b) {
    return a + b;
  }

  int subtracao(int a, int b) => a - b;
  int multiplicacao(int a, int b) => a * b;
  double divisao(int a, int b) => a / b;
  print(adicao(4, 2));
  print(subtracao(4, 2));
  print(multiplicacao(4, 2));
  print(divisao(6, 2));
}
